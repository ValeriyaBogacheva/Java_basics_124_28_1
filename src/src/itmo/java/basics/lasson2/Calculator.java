package src.itmo.java.basics.lasson2;

public class Calculator {
    public double sum(double a1, double a2){
        return a1 + a2;
    }
    public long sum(long a1, long a2){
        return a1 + a2;
    }
    public int sum(int a1, int a2){
        return a1 + a2;
    }
    public double division(double a1, double a2){
        return a1 / a2;
    }
    public double division(long a1, long a2){
        return (double) a1 / a2;
    }
    public double multi(double a1, double a2){
        return a1 * a2;
    }
    public long multi(long a1, long a2){
        return a1 * a2;
    }
    public int multi(int a1, int a2){
        return a1 * a2;
    }
    public double substract(double a1, double a2){
        return a1 - a2;
    }
    public long substract(long a1, long a2){
        return a1 - a2;
    }
    public int substract(int a1, int a2){
        return a1 - a2;
    }
}
